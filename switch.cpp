#include <iostream>
using namespace std;

int main(){
    char button;
    cin>>button;
    switch(button){
        case 'a':
        cout<<"ILY"<<endl;
        break; 

        case 'b':
        cout<<"IMY"<<endl;
        break; 

        case 'c':
        cout<<"IHY"<<endl;
        break; 

        case 'd':
        cout<<"ITY"<<endl;
        break; 

        default:
        cout<<"IFY"<<endl;
        break;
    }
}